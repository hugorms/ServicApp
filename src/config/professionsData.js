// Obtener especialidades de una profesión
export const getSpecialties = (profession) => {
  return PROFESSIONS_CATALOG[profession]?.specialties || [];
};

// Obtener icono de una profesión
export const getProfessionIcon = (profession) => {
  return PROFESSIONS_CATALOG[profession]?.icon || '🔧';
};

// Validar si una profesión existe
export const isProfessionValid = (profession) => {
  return PROFESSIONS_LIST.includes(profession);
};
